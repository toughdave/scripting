CREATE TABLE student_records_source (
  student_id VARCHAR(20),
  first_name VARCHAR(100),
  last_name VARCHAR(100),
  email VARCHAR(255),
  department VARCHAR(120),
  admission_year INT,
  score INT,
  status VARCHAR(30),
  due_date DATE,
  completed_at DATE
);

